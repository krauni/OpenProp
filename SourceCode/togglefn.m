function togglefn(hObject,ED)

    global PlotPanels Toggle;

    for index = 1 : length(Toggle)

        if get(Toggle(index),'value')
            set(PlotPanels(index),'visible','on');
        else
            set(PlotPanels(index),'visible','off');
        end
    end

end

